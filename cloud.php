        <h2>Cloud Monitoring and Control</h2>

        <h3>Description</h3>

                <p style="padding-left: 1em">
                Nowadays, more and more the industry and market depend on cloud-based infrastructures for delivering IT services.

                To drive this increasing demand, cloud providers made huge investments supporting the public cloud paradigm,
                thanks to which any customer can take advantage of virtual resources at varying performance and cost.
                </p>

                <p style="padding-left: 1em">
                However, the dependence of the industry on these infrastructures
                has grown much faster than our understanding of the performance limits and dynamics of these environments.

                In fact,
                the adoption of sophisticated virtualization strategies for sharing resources among a great
                number of largely uncoordinated and mutually untrusted customers
                can cause non-negligible performance penalties.

                While cloud customers are not aware of the complexity of similar mechanisms,
                the Quality of the Service (QoS) they perceive is deeply affected by them.

                Indeed, cloud providers often provide no or only coarse grained information about the
                performance a customer should expect from the cloud.

                Hence the only viable solution to the customer is to monitor these complex environments through non-cooperative approaches
                - i.e. without relying on information restricted to the provider.
                </p>

                <p style="padding-left: 1em">
                Moreover, the information provided by non-cooperative approaches,
                properly integrated together with performance indicators directly provided by the providers,
                are of paramount importance to conveniently control virtual resources.
                </p>

                <p style="padding-left: 1em">
                Our work aims at
                characterizing cloud performance by adopting provider-independent approaches.
                In more details, we focus on the performance of the resource that interconnects all the others
                (i.e. the cloud high-performance network).

                We are developing novel strategies to
                automatically control
                virtual resource deployment in cloud environments,
                and aimed at deploying quality-assured services to final users.
                </p>


                <p style="padding-left: 1em">
                Our experimental activities are performed
                by taking advantage of the cloud services offered by the leading public cloud providers:


